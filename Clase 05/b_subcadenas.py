"""
Subcadenas (sub-strings)
Slicing (rebanando)
"""

cadena = "Aprendiendo Python"
print(f"longitud de cadena es {len(cadena)}")
# print(cadena[-2])
subcadena = cadena[5:11]  # str[indice_inicio : indice_fin]
print(subcadena)
subcadena = cadena[:10]  # str[indice_inicio : indice_fin]
print(subcadena)
subcadena = cadena[5:]  # str[indice_inicio : indice_fin]
print(subcadena)
